package com.kodilla.testing.calculator;

public class Calculator {

    public int add(int a, int b) {
        System.out.print("Calculation : " + a + " + " + b + " = ");
        return a + b;
    }

    public int subtract(int a, int b) {
        System.out.print("Calculation : " + a + " - " + b + " = ");
        return a - b;
    }
}
